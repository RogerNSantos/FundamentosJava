package fundamentos;

public class Inferencia {

	public static void main (String[] args) {
		
		double a = 4.6;// DECLARANDO E INICIALIZAÇÃO DA VARIÁVEL
		System.out.println(a);
		
		a = 12;
		System.out.println(a);
		
		//a = "Rogério";
		
		var b = 4.5;
		System.out.println(b);
		
		var c= "Palavra";
		System.out.println(c);
		
		c = "Outra palavra";
		System.out.println(c);
		
		double  d;// VARIÁVEL FOI DECLARADA
		d = 123.75;//VARIÁVEL FOI INICIALIZADA
		System.out.println(d);//VARIAVEL SENDO USADA!
	}
}
